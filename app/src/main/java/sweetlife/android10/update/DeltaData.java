package sweetlife.android10.update;

public class DeltaData {
	private String mExchangePlan;
	private String mTo;
	private String mFrom;
	private int mMessageNo;
	private int mReceivedNo;

	public String getExchangePlan() {
		return mExchangePlan;
	}
	public void setExchangePlan(String exchangePlan) {
		mExchangePlan = exchangePlan;
	}
	public String getTo() {
		return mTo;
	}
	public void setTo(String to) {
		mTo = to;
	}
	public String getFrom() {
		return mFrom;
	}
	public void setFrom(String from) {
		mFrom = from;
	}
	public int getMessageNo() {
		return mMessageNo;
	}
	public void setMessageNo(int messageNo) {
		mMessageNo = messageNo;
	}
	public int getReceivedNo() {
		return mReceivedNo;
	}
	public void setReceivedNo(int receivedNo) {
		mReceivedNo = receivedNo;
	}
}
